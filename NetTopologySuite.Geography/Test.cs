        [Test]
        public void TestPointToPointDistance()
        {
            var f = GeographyFactory.WGS84;
            
            // Aurich
            var ptAurich = f.CreatePoint(new LatLon(53.47084, 7.48467));

            // Emden
            var ptEmden = f.CreatePoint(new LatLon(53.35975, 7.20642));

            double dAE = ptAurich.Distance(ptEmden);
            Console.WriteLine($"Point to point distance Aurich <-> Emden: {(dAE/1000d):F1}km");

            Assert.That(dAE > 20000);
        }

        [Test]
        public void TestLineStringLength()
        {
            var f = GeographyFactory.WGS84;
            var g = f.CreateLineString(_routeEmdenAurich);

            double l = g.Length / 1000d;
            Console.WriteLine($"Route Emden-Aurich via Ihlow is {l:F1}km long.");
            Assert.That(l, Is.EqualTo(27.4).Within(0.25));

        }

        [Test]
        public void TestPolygonArea()
        {
            var f = GeographyFactory.WGS84;
            var g = f.CreatePolygon(_areaTownOfAurich);

            double area = g.Area/1000000;
            Console.WriteLine($"Area of town of Aurich {area:F1}km²");
            Assert.That(area, Is.EqualTo(197.2).Within(1));
        }

        [Test]
        public void TestIntersection()
        {
            var f = GeographyFactory.WGS84;
            var a = f.CreatePolygon(_areaTownOfAurich);
            var r = f.CreateLineString(_routeEmdenAurich);

            var rInA = a.Intersection(r);

            Console.WriteLine($"Route Emden-Aurich via Ihlow has {rInA.Length/1000:F1}km on territory of Aurich.");

            Assert.That(rInA, Is.Not.Null);
            Assert.That(rInA.Factory, Is.EqualTo(f));
            Assert.That(rInA.Length, Is.LessThan(r.Length));

        }

